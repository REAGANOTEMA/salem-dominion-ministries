<!-- Footer JavaScript -->
<script>
document.addEventListener('DOMContentLoaded', function() {
    // Add hover effects to footer links
    const footerLinks = document.querySelectorAll('.footer-links a, .service-links li');
    footerLinks.forEach(link => {
        link.addEventListener('mouseenter', function() {
            this.style.transform = 'translateX(5px)';
        });
        link.addEventListener('mouseleave', function() {
            this.style.transform = 'translateX(0)';
        });
    });
    
    // Newsletter form validation
    const newsletterForm = document.querySelector('.newsletter-form');
    if (newsletterForm) {
        newsletterForm.addEventListener('submit', function(e) {
            e.preventDefault();
            const email = this.querySelector('input[type="email"]').value;
            
            if (email) {
                // Show success message
                const successMessage = document.createElement('div');
                successMessage.className = 'alert alert-success alert-custom';
                successMessage.innerHTML = '<i class="fas fa-check-circle me-2"></i>Thank you for subscribing!';
                this.parentNode.appendChild(successMessage);
                
                // Clear form
                this.reset();
                
                // Remove message after 3 seconds
                setTimeout(() => {
                    successMessage.remove();
                }, 3000);
            }
        });
    }
    
    // Add floating animation to buttons
    const whatsappButton = document.querySelector('.whatsapp-button');
    const supportButton = document.querySelector('.support-button');
    
    if (whatsappButton) {
        setInterval(() => {
            whatsappButton.style.transform = 'translateY(-5px)';
            setTimeout(() => {
                whatsappButton.style.transform = 'translateY(0)';
            }, 500);
        }, 3000);
    }
    
    if (supportButton) {
        setInterval(() => {
            supportButton.style.transform = 'translateY(-3px)';
            setTimeout(() => {
                supportButton.style.transform = 'translateY(0)';
            }, 500);
        }, 4000);
    }
});
</script>
